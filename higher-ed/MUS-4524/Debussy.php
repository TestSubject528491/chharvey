		<h3>Alexis Weissenberg, 1985</h3>
	</header>
	<p>This movement is an overflow of notes and chords. Rather than focusing on individual aspects of the music, the performer aims for the overall atmosphere. Polyrhythms and other fast runs don't have to be played exactly correctly, and the bigger picture is what's important. The piece has many different sections that seem to be put together in an arbitrary manner, much like an unpredictable rainstorm. The transitions between the segments do flow smoothly, however. There is no defined form, but the melodic theme recurs throughout the movement. Most of the texture of the piece consists of simple homophonic melody and accompaniment, although the melody often switches between hands. The movement moves in and out of different modes, usually among major, minor, and augmented. There doesn't seem to be a predictable change in tonal center, but the harmonic structure is always present, even with a lot of chromaticism in a few places.</p>
	<p>The performer exhibits great control and clarity, even at a fast pace. His choice of dynamics contradicts the text at some points, but otherwise he does a good job showing contrast between loud and soft. Weissenberg ends the piece in a huge wash of sound, making it almost impossible to interpret the small details of the music.</p>
