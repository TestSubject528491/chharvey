<section id="BWV779">
	<header>
		<h2 class="work"><cite>Invention No. 8 in F Major (BWV 779)</cite></h2>
		<h3>Christopher Taylor, 2006</h3>
	</header>
	<p>Bach Inventions are short counterpoint pieces with two voices, and <cite>No. 8</cite> is an excellent example. The subject, answer, and counter-subject interact in such a way that they fit together to sound like melody and harmony. Taylor makes the entrances of each subject plainly obvious to the listener so he/she can identify it at once. He swiftly moves through this upbeat invention, only pausing slightly at cadence points.</p>
</section>

<section id="BWV866">
	<header>
		<h2 class="work"><cite>Prelude &amp; Fugue No. 21 in B-Flat Major (BWV 866)</cite> [from <cite>WTC Book I</cite>]</h2>
		<h3>Angela Hewitt, 2007</h3>
	</header>
	<p>Hewitt does a great job emphasizing the outer voices in the homophonic sixteenth-note passages in the <cite>Prelude</cite>. Her dynamic contrast adds excitement to the piece, even when marked <i class="lang" lang="it">piano</i>. She also has excellent contrast in articulation in the <cite>Fugue</cite>. Like Taylor, she makes the entrances of subject and answer obvious. Her performance is always
moving in some direction, never stationary, even during <i class="lang">stretto</i>s and episodes.</p>
</section>

<section id="BWV876">
	<header>
		<h2 class="work"><cite>Prelude &amp; Fugue No. 7 in E-Flat Major (BWV 876)</cite> [from <cite>WTC Book II</cite>]</h2>
		<h3>Angela Hewitt, 2007</h3>
	</header>
	<p>Although the <cite>Prelude</cite> is a quick-moving piece, Hewitt remains calm and composed, and keeps a consistent tempo. She adds variety to articulation patterns throughout the <cite>Prelude</cite>. She announces the return of the piece's theme in a blissful manner and with a change in articulation. Again, Hewitt makes this difficult four-voice <cite>Fugue</cite> seem easy to play. The <b>Subject</b> appears in all four voices multiple times, and can be easily identified. Her tempo is slightly faster than marked, but she pulls it off very well.</p>
</section>

