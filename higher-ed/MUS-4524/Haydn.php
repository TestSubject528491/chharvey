<section id="XVI23">
	<header>
		<h2 class="work"><cite>Sonata No. 38 in F Major, H. XVI/23</cite> (1773)</h2>
		<h3>Marc-Andr&eacute; Hamelin, 2007</h3>
	</header>
	<p>Haydn wrote his music in a way that is open to interpretation. In this sonata, he leaves out almost all dynamics and articulation. Haydn has a talent for surprising the listener with <i class="lang" lang="it">subito</i> dynamics and other musical formatting. Hamelin plays with a great contrast in articulation, ranging from spiky <i class="lang" lang="it">staccati</i> to fluid <i class="lang" lang="it">legati</i>. At times, he may hold some notes longer than expected. He seldom uses the pedal, which is accurate for the Classical time period. While there is no correct answer, Hamelin's dynamic markings differ from mine. He plays the third movement impressively fast.</p>
</section>

<section id="XVI28">
	<header>
		<h2 class="work"><cite>Sonata No. 43 in E-flat Major, H. XVI/28</cite> (1776)</h2>
		<h3>Lola Odiaga in 1995</h3>
	</header>
	<p>Odiaga performs this sonata on the fortepiano, an instrument suitable for the context in which the piece was written. For some reason, the fortepiano is tuned one semitone flat, so the piece sounds as if in D Major.</p>
	<p>Her choice of articulation contradicts my own, but is acceptable for a Haydn sonata. Ornaments, trills, and turns are taken faster than normal, and in addition, placed before the beat rather than on the beat. Her choice of dynamics is conventional and expected by the listener.</p>
	<p>The second movement is played in a hasty manner. Odiaga sees not to pay much attention to phrasing or articulation, and every turn is played too fast and before the beat, as grace notes. The melody in the Trio section is <i class="lang" lang="it">legato</i> and slurred throughout. She ends every movement with a <i class="lang" lang="it">molto ritardando</i>, which would be appropriate for maybe a Baroque-era piece, but I personally choose not to alter the tempo when closing the movements.</p>
	<p>The third movement, marked &ldquo;<i class="lang" lang="it">Finale: Presto</i>,&rdquo; is played carefully and almost tentatively. The performer did an excellent job with articulation and liveliness in this movement, although lacking in dynamic variation. The entire third movement builds speed progressively throughout the movement, which may or may not be intentional.</p>
</section>

