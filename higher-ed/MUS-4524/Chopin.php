		<h2 class="work"><cite>Scherzo No. 1 in B minor, Op. 20</cite> (1832)</h2>
		<h3>Emanuel Ax, 1988</h3>
	</header>
	<p>The Scherzo in B minor is divided into three main sections set in ABA form, with the A sections as scherzos and the middle section as a Trio. The Scherzo sections are furious ad rapid, and the performer brings out the violence in Chopin's music. His emotion is apparent through his playing. He demonstrates great contrast in dynamics and articulation, making the music dance as lively as possible. However, due to the extreme number of repetitions written in the piece, the performer does not provide noticeable variation across repeated sections.</p>
	<p>Ax brings out the emotional changes via dynamics and tempo changes. He prolongs the Trio for as long as possible, creating a sense of anxiety in the listener, who longs for the expressive and delicate tonic chord that releases the built-up tension.</p>
	<p>The Trio section is the polar opposite of the scherzo sections. Ax brings out as much, if not more, emotion to this section, but an emotion of tenderness and love rather than ferocity and angst. The sections also differ via thematic material: while the Scherzo is more motivic, the Trio contains a sweet lyrical melody in the middle voice that depicts mother and child.</p>
	<p>The performer builds tension towards the end of the Trio, bringing out dissonances and non-chord tones. He prepares us for the return of the Scherzo with a <i class="lang">molto ritardando</i> and the reappearance of the introduction.</p>
	<p>The return of the scherzo section is no different than the exposition, except that it is shorthand and with a Coda added. Ax again anticipates the Coda, building up speed. The Coda is like a warped waltz with dissonant tones and a humorous feel. The performer executes the trills, arpeggios, and scales with extreme precision.</p>
